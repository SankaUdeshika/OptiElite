    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // Update Brand
        try {
            int Selectrow = jTable1.getSelectedRow();
            String brandNme = jTextField1.getText();

            if (Selectrow == -1) {

                JOptionPane.showMessageDialog(this, "Please Select a brand row", "Error", JOptionPane.ERROR_MESSAGE);
            } else {

                if (brandNme.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Please Enter Brand Name", "Error", JOptionPane.ERROR_MESSAGE);

                } else {
                    ResultSet rs = MySQL.execute("SELECT * FROM `brand` WHERE `brand_name` = '" + brandNme + "' ");
                    if (!rs.next()) {

                        String brandID = String.valueOf(jTable1.getValueAt(Selectrow, 0));
                        MySQL.execute("UPDATE `brand` SET `brand_name` = '" + brandNme + "'  WHERE `id` = '" + brandID + "' ");
                        JOptionPane.showMessageDialog(this, "Brand Updated Success", "Success", JOptionPane.OK_OPTION);
                        Refresh();
                    } else {
                        JOptionPane.showMessageDialog(this, "Already Registerd this Brand", "Error", JOptionPane.ERROR_MESSAGE);

                    }
                }

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton11ActionPerformed

