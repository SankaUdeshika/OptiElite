    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // Delete Brand
        int selectRow = jTable1.getSelectedRow();

        if (selectRow == -1) {
            JOptionPane.showMessageDialog(this, "Please Select Brand row", "Error", JOptionPane.ERROR_MESSAGE);

        } else {

            try {
                String brand_id = String.valueOf(jTable1.getValueAt(selectRow, 0));
                ResultSet rs = MySQL.execute("SELECT * FROM `product` INNER JOIN `brand` ON `product`.`brand_id` = `brand`.`id` WHERE `brand`.`id` = '" + brand_id + "' ");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Can't Delete this category, Already pruchese items on this category", "Success", JOptionPane.OK_OPTION);
                } else {
                    MySQL.execute("DELETE FROM `brand` WHERE `id` = '" + brand_id + "' ");
                    JOptionPane.showMessageDialog(this, "Brand Delete Success", "Success", JOptionPane.OK_OPTION);
                    logger.log(Level.INFO, "Brand Delete Success");

                    Refresh();;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Can't Delete this brand", "Success", JOptionPane.OK_OPTION);
            }

        }


    }//GEN-LAST:event_jButton10ActionPerformed

