    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // Delete Branch

        int selectedROw = jTable1.getSelectedRow();
        String branchID = String.valueOf(jTable1.getValueAt(selectedROw, 0));
        
        MySQL.execute("DELETE FROM `location` WHERE `id` = '" + branchID + "'");
        JOptionPane.showMessageDialog(this, "Delete Success", "Deleted", JOptionPane.OK_OPTION);
        refresh();

    }//GEN-LAST:event_jButton5ActionPerformed

