    public void LoadPrescription() {
        try {
            ResultSet rs = MySQL.execute("SELECT * FROM `prescription_details` INNER JOIN `users` ON `users`.`id` = `prescription_details`.`users_id` INNER JOIN `location` ON `location`.`id` = `users`.`location_id` INNER JOIN `customer` ON `customer`.`mobile` = `prescription_details`.`customer_mobile` ");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("job_no"));
                v.add(rs.getString("mobile"));
                v.add(rs.getString("name"));
                v.add(rs.getString("prescripiton_date"));
                v.add(rs.getString("fname"));
                v.add(rs.getString("location_name"));
                v.add(rs.getString("location_name"));

                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

