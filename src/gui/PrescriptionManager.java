    private void nicFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nicFieldKeyReleased
        // Key Relesed Search by NIC
        String NIC = nicField.getText();

        mobileField.setText("");
        nameField.setText("");

        try {
            ResultSet rs = MySQL.execute("SELECT * FROM prescription_details "
                    + "INNER JOIN customer ON customer.mobile = prescription_details.customer_mobile "
                    + "INNER JOIN users ON users.id = prescription_details.users_id "
                    + "INNER JOIN location ON customer.location_id = location.id "
                    + "WHERE `customer`.`nic` LIKE '%" + NIC + "%' ");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                String fistName = rs.getString("fname");
                String lastName = rs.getString("lname");
                String fullName = fistName + " " + lastName;

                Vector v = new Vector();
                v.add(rs.getString("job_no"));
                v.add(rs.getString("customer_mobile"));
                v.add(rs.getString("name"));
                v.add(rs.getString("prescripiton_date"));
                v.add(fullName);
                v.add(rs.getString("location_name"));

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_nicFieldKeyReleased

