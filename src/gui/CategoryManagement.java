    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // Update Category
        int selectRow = jTable1.getSelectedRow();
        String subCategory_name = jTextField1.getText();

        if (selectRow == -1) {
            JOptionPane.showMessageDialog(this, "Please Select a Category Row", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String Category_id = String.valueOf(jTable1.getValueAt(selectRow, 0));

            try {
                ResultSet rs = MySQL.execute("SELECT * FROM `sub_category` WHERE  `sub_category`.`sub_category` = '" + subCategory_name + "' ");
                if (!rs.next()) {
                    MySQL.execute("UPDATE `sub_category` SET `sub_category` = '" + subCategory_name + "' WHERE `id` = '" + Category_id + "' ");
                    JOptionPane.showMessageDialog(this, "Updated Sub Category", "Success", JOptionPane.OK_OPTION);
                    Refresh();
                } else {
                    JOptionPane.showMessageDialog(this, "Already Registerd this sub_category", "Error", JOptionPane.ERROR_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton5ActionPerformed

