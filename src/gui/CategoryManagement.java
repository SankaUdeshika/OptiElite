    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // Add Category
        String categoryName = jTextField1.getText();
        int Category_id = jComboBox1.getSelectedIndex();

        if (categoryName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Category Name", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (Category_id == 0) {
            JOptionPane.showMessageDialog(this, "Please Select a Category", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                ResultSet rs = MySQL.execute("SELECT * FROM `sub_category` WHERE `sub_category` = '" + categoryName + "' ");
                if (!rs.next()) {
                    MySQL.execute("INSERT INTO `sub_category` (`category_id`,`sub_category`) VALUES ('" + Category_id + "','" + categoryName + "')");
                    JOptionPane.showMessageDialog(this, "Category Added", "Success", JOptionPane.OK_OPTION);
              
                    Refresh();
                } else {
                    JOptionPane.showMessageDialog(this, "Already Registerd this sub Caetgory", "Error", JOptionPane.ERROR_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton9ActionPerformed

    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
//         Delete Brand
        int selectRow = jTable1.getSelectedRow();

        if (selectRow == -1) {
            JOptionPane.showMessageDialog(this, "Please Select Sub Category row", "Error", JOptionPane.ERROR_MESSAGE);

        } else {
            try {
                String sub_category_id = String.valueOf(jTable1.getValueAt(selectRow, 0));
                ResultSet rs = MySQL.execute("SELECT * FROM `invoice_item` INNER JOIN `stock` ON `stock`.`id` = `invoice_item`.`stock_id` INNER JOIN `product` ON `product`.`id` = `stock`.`product_id` WHERE `product`.`sub_category_id` =  '" + sub_category_id + "' ");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Can't Delete this category, Already pruchese items on this category", "Success", JOptionPane.OK_OPTION);
                } else {
                    MySQL.execute("DELETE FROM `sub_category` WHERE `id` = '" + sub_category_id + "' ");
                    JOptionPane.showMessageDialog(this, "Category Delete Success", "Success", JOptionPane.OK_OPTION);
                    Refresh();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Can't Delete this brand", "Success", JOptionPane.OK_OPTION);
            }
        }

    }//GEN-LAST:event_jButton10ActionPerformed

    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // Category Change
        try {
            int category_id = jComboBox2.getSelectedIndex();
            ResultSet rs = MySQL.execute("SELECT * FROM `sub_category` INNER JOIN `category` ON `category`.`id` = `sub_category`.`category_id` WHERE  `category`.`id` LIKE '" + category_id + "' ");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("sub_category"));
                v.add(rs.getString("Category"));
                dtm.addRow(v);
            }

        } catch (SQLException se) {
            se.printStackTrace();
            JOptionPane.showMessageDialog(this, "Please Check Your Internet Conneciton", "Connection Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Something Wrong Please Try again Later", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

